    /**
     * jButton1 - Download new version <br/>
     * Opens the Browser to download a new version of jMM
     * @param evt the triggered event
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //get requiered resources
        List<String> resources = UpdateManager.getRequieredResources();
        
        if((resources != null) && (MovieManagerGUI.getInstance().saveAndDispose(true))){        
            //Add Shutdown process
            Runtime.getRuntime().addShutdownHook(UpdateManager.createSilentUpdateProcess((LinkedList<String>) resources));
            System.exit(0);      
        }else{
            //full update requiered
            try { 
                OperatingSystem.openBrowser(updateURL);
            } catch (IOException | URISyntaxException ex) {
                LOG.error("URL: " + updateURL + " could not be opened in the browser.", ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

