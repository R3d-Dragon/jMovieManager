    /**
     * Performs an update Check and enables the download button
     * @return true Update ist verf√ºgbar <br/>
     * false sonst
     */
    public boolean updateAvailable(){
        double latestVersion = UpdateManager.getLatestVersion();
        boolean updateAvailable = false;
        if(latestVersion > Settings.currentVersion){
            //if file found, enable button
            jButton1.setEnabled(true);
            jPanel2.add(jLabel7);
            updateAvailable = true;
            jLabel1.setText(bundle.getString("UpdateGUI.jLabel1.update"));
            jLabel6.setText(bundle.getString("UpdateGUI.jLabel6.update"));
            this.setTitle(bundle.getString("UpdateGUI.headline.update"));
        }
        jLabel5.setText(String.valueOf(latestVersion));
        return updateAvailable;
    }    
    
